import java.util.Scanner;

public class EXERCICIO6 {
	public static void main(String[]args) {
		
		Scanner read = new Scanner(System.in);
		
		double x1, x2, y1, y2, x, y, D;
		
		System.out.print("Insira o Ponto inicial: ");
		x1 = read.nextInt();
		y1 = read.nextInt();
		
		System.out.print("\nInsira o Ponto final: ");
		x2 = read.nextInt();
		y2 = read.nextInt();
		
		x = x2-x1;
		y = y2-y1;
		
		D = Math.sqrt(x*x + y*y);
		
		System.out.print("\nA distancia entre os dois pontos é: "+ D);
		
		
		read.close();
	}
}
