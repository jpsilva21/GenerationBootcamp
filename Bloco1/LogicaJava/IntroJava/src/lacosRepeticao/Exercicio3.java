package lacosRepeticao;

import java.util.Scanner;

public class Exercicio3 {

	public static void main(String[] args) {
		Scanner read = new Scanner(System.in);

	int idade=0, maior=0, menor=0;
			
		while (idade!=-99) {
			System.out.println("Insira a idade: ");
			idade= read.nextInt();
			
			if (idade<21 && idade>0) {
				menor++;
			}
			
			else if (idade>50) {
				maior++;
			}
			read.close();
		}
		System.out.println(menor+" pessoa(s) são menores de 21 anos");
		System.out.println(maior+" pessoa(s) são maiores de 50 anos");
	
	}
}